// Parses Content/vraagbank.md (the owner's own question bank, 120 items across LEZ/LUI/SPR/SCH,
// A1-B2) into a SQL migration: supabase/migrations/0010_vraagbank_content.sql
// Run with: node supabase/content-src/parse-vraagbank.mjs
//
// Mapping: one lesson per item, grouped by skill (from the code prefix) and level (from **Niveau:**).
//  - LEZ  -> reading_text {text} + open_text {question} answer {answer, explanation}
//  - LUI  -> listening_clip {script} + multiple_choice {question, options} answer {correctIndex, explanation}
//  - SPR  -> speaking_prompt {scenario, expectedPoints, modelAnswer}
//  - SCH  -> writing_task {instructions, minWords?, modelAnswer}

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const SRC = join(__dirname, "..", "..", "Content", "vraagbank.md");
const OUT = join(__dirname, "..", "migrations", "0010_vraagbank_content.sql");

const SKILL_BY_PREFIX = { LEZ: "lezen", LUI: "luisteren", SPR: "spreken", SCH: "schrijven" };
const VALID_LEVELS = ["A1", "A2", "B1", "B2"];

const md = readFileSync(SRC, "utf8");
const lines = md.split(/\r?\n/);

// Split into item blocks by "## CODE-NN — Title" headers.
const headerRe = /^##\s+(LEZ|LUI|SPR|SCH)-(\d+)\s+[—-]\s+(.+?)\s*$/;
const blocks = [];
let current = null;
for (const line of lines) {
  const m = line.match(headerRe);
  if (m) {
    current = { prefix: m[1], number: parseInt(m[2], 10), title: m[3], body: [] };
    blocks.push(current);
  } else if (current) {
    if (/^#\s+/.test(line)) current = null; // module header ends the item
    else current.body.push(line);
  }
}

function esc(s) {
  return s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function inline(body, label) {
  const re = new RegExp(`^\\*\\*${esc(label)}[^\\n:]*:\\*\\*[ \\t]*(.*)$`, "m");
  const m = body.match(re);
  return m && m[1].trim() ? m[1].trim() : null;
}

function quoteAfter(body, labelPrefix) {
  const arr = body.split("\n");
  for (let i = 0; i < arr.length; i++) {
    if (new RegExp(`^\\*\\*${esc(labelPrefix)}`).test(arr[i].trim())) {
      let j = i + 1;
      while (j < arr.length && arr[j].trim() === "") j++;
      const out = [];
      while (j < arr.length && arr[j].trim().startsWith(">")) {
        out.push(arr[j].trim().replace(/^>\s?/, "").replace(/\s+$/, ""));
        j++;
      }
      const text = out.join("\n").trim();
      return text || null;
    }
  }
  return null;
}

function bulletsAfter(body, labelPrefix) {
  const arr = body.split("\n");
  for (let i = 0; i < arr.length; i++) {
    if (new RegExp(`^\\*\\*${esc(labelPrefix)}`).test(arr[i].trim())) {
      const out = [];
      let j = i + 1;
      while (j < arr.length) {
        const t = arr[j].trim();
        if (t === "") {
          j++;
          continue;
        }
        if (t.startsWith("- ")) {
          out.push(t.slice(2).replace(/[;.]\s*$/, "").trim());
          j++;
          continue;
        }
        break;
      }
      return out;
    }
  }
  return [];
}

function mcOptions(body) {
  const out = [];
  for (const line of body.split("\n")) {
    const m = line.trim().match(/^([A-D])\.\s+(.+?)\s*$/);
    if (m) out.push(m[2]);
  }
  return out;
}

function parseMinWords(text) {
  if (!text) return undefined;
  const range = text.match(/(\d+)\s*(?:tot|-|–|—)\s*(\d+)\s*woord/i);
  if (range) return parseInt(range[1], 10);
  const single = text.match(/(\d+)\s*woord/i);
  if (single) return parseInt(single[1], 10);
  return undefined;
}

function sqlStr(v) {
  if (v === null || v === undefined) return "null";
  return "'" + String(v).replace(/'/g, "''") + "'";
}
function sqlJson(obj) {
  return "'" + JSON.stringify(obj).replace(/'/g, "''") + "'::jsonb";
}
function lessonUuid(index) {
  return `b0000000-0000-4000-8000-${String(index + 1).padStart(12, "0")}`;
}

const parts = [
  "-- Vraagbank content (owner's own material). Generated by parse-vraagbank.mjs.",
  "-- Run after 0008_lesson_rich_content.sql. One lesson per item, grouped by skill + level.",
  "",
];

let index = 0;
let count = 0;
const skipped = [];
const freeGiven = new Set(); // one free preview lesson per skill

for (const block of blocks) {
  const body = block.body.join("\n");
  const skill = SKILL_BY_PREFIX[block.prefix];
  const level = inline(body, "Niveau");
  if (!skill || !level || !VALID_LEVELS.includes(level)) {
    skipped.push(`${block.prefix}-${block.number} (niveau/skill onbekend)`);
    continue;
  }

  const id = lessonUuid(index++);
  const situation =
    inline(body, "Situatie voor cursist") || inline(body, "Situatie") || null;
  const isFree = level === "A1" && !freeGiven.has(skill);
  if (isFree) freeGiven.add(skill);
  const sortOrder = 100 + block.number;

  const exercises = [];

  if (block.prefix === "LEZ") {
    const text = quoteAfter(body, "Tekst voor cursist");
    const question = inline(body, "Vraag");
    const answer = inline(body, "Correct antwoord");
    const explanation = inline(body, "Uitleg");
    if (text) {
      exercises.push({ type: "reading_text", prompt: "Lees de tekst.", content: { text }, answer: null });
    }
    if (question) {
      exercises.push({
        type: "open_text",
        prompt: "Beantwoord de vraag.",
        content: { question },
        answer: { answer: answer ?? "", explanation: explanation ?? undefined },
      });
    }
  } else if (block.prefix === "LUI") {
    const script = quoteAfter(body, "Luisterscript");
    const question = inline(body, "Vraag");
    const options = mcOptions(body);
    const correctRaw = inline(body, "Correct antwoord");
    const letter = correctRaw ? correctRaw.trim().charAt(0).toUpperCase() : "A";
    const correctIndex = Math.max(0, "ABCD".indexOf(letter));
    const explanation = inline(body, "Luisterclue") || inline(body, "Uitleg") || undefined;
    if (script) {
      exercises.push({
        type: "listening_clip",
        prompt: situation ?? "Beluister het fragment.",
        content: { script },
        answer: null,
      });
    }
    if (question && options.length >= 2) {
      exercises.push({
        type: "multiple_choice",
        prompt: "Kies het juiste antwoord.",
        content: { question, options },
        answer: { correctIndex, explanation },
      });
    }
  } else if (block.prefix === "SPR") {
    const situatie = inline(body, "Situatie");
    const opdrachtInline = inline(body, "Spreekopdracht");
    const bullets = bulletsAfter(body, "Spreekopdracht");
    const essential = inline(body, "Essentiële inhoudspunten");
    const modelAnswer = quoteAfter(body, "Modelantwoord");
    const scenario = [situatie, opdrachtInline].filter(Boolean).join(" ");
    const expectedPoints =
      bullets.length > 0
        ? bullets
        : essential
          ? essential.split(/,|\ben\b/).map((s) => s.trim()).filter(Boolean)
          : undefined;
    exercises.push({
      type: "speaking_prompt",
      prompt: "Oefen hardop.",
      content: { scenario: scenario || block.title, expectedPoints, modelAnswer: modelAnswer ?? undefined },
      answer: null,
    });
  } else if (block.prefix === "SCH") {
    const situatie = inline(body, "Situatie");
    const opdrachtInline = inline(body, "Opdracht");
    const bullets = bulletsAfter(body, "Opdracht");
    const modelAnswer = quoteAfter(body, "Modelantwoord");
    const instructionParts = [situatie, opdrachtInline].filter(Boolean);
    let instructions = instructionParts.join(" ");
    if (bullets.length > 0) instructions += "\n\n- " + bullets.join("\n- ");
    const minWords = parseMinWords([opdrachtInline, ...bullets].filter(Boolean).join(" "));
    exercises.push({
      type: "writing_task",
      prompt: "Schrijf de tekst.",
      content: { instructions: instructions || block.title, minWords, modelAnswer: modelAnswer ?? undefined },
      answer: null,
    });
  }

  if (exercises.length === 0) {
    skipped.push(`${block.prefix}-${block.number} (geen oefeningen geparseerd)`);
    index--; // reuse the id slot
    continue;
  }

  count++;
  parts.push(`-- ${block.prefix}-${block.number} (${level}) — ${block.title}`);
  parts.push(
    "insert into lessons (id, level_code, skill_code, title, description, sort_order, is_free) values (",
  );
  parts.push(`  ${sqlStr(id)}, ${sqlStr(level)}, ${sqlStr(skill)}, ${sqlStr(block.title)},`);
  parts.push(`  ${sqlStr(situation)}, ${sortOrder}, ${isFree}`);
  parts.push(");");

  exercises.forEach((ex, exIndex) => {
    parts.push(
      "insert into exercises (lesson_id, type, prompt, content, correct_answer, sort_order) values (",
    );
    parts.push(
      `  ${sqlStr(id)}, ${sqlStr(ex.type)}, ${sqlStr(ex.prompt)}, ${sqlJson(ex.content)}, ${
        ex.answer ? sqlJson(ex.answer) : "null"
      }, ${exIndex + 1}`,
    );
    parts.push(");");
  });
  parts.push("");
}

writeFileSync(OUT, parts.join("\n"), "utf8");
console.log(`Parsed ${count} lessons from ${blocks.length} items into ${OUT}`);
if (skipped.length > 0) {
  console.log(`Skipped ${skipped.length}:`);
  for (const s of skipped) console.log("  - " + s);
}
